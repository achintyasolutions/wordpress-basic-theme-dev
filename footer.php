            console.log(jQuery(this).is(":checked"));
            if (jQuery(this).is(":checked")){
                jQuery(this).parent('label').toggleClass('chip--selected');
                jQuery(this).prop(":checked", true);
            } 
            
            if (!jQuery(this).is(":checked")){
                jQuery(this).parent('label').toggleClass('chip--selected');
                jQuery(this).prop(":checked", false);
            } 
        });

        jQuery('#formResetbtn').on('click', function(){
          jQuery('input[name="choices-bedrooms"]').prop('checked', false).parent('label').removeClass('chip--selected');
