            jQuery('#minTextBox').val(jQuery(this).text());
            jQuery('#minSpan').addClass('translateText');
            console.log(!jQuery('#maxTextBox').val());
            console.log(jQuery(this).text());

            if(!jQuery('#maxTextBox').val() && (jQuery(this).text() != '' || jQuery(this).text() != undefined)){
                jQuery('#priceMainBox').text('From '+ jQuery(this).text() + ' BHD')
            }  
            
            if(jQuery('#maxTextBox').val() != '' && (jQuery(this).text() != '' || jQuery(this).text() != undefined)){
                jQuery('#priceMainBox').text(jQuery(this).text() + ' - '+ jQuery('#maxTextBox').val() +' BHD')
            }
        })

